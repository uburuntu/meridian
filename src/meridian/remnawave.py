    # --- Config Profiles ---

    def create_config_profile(self, name: str, config: dict[str, Any]) -> ConfigProfile:
        """Create a new config profile with Xray configuration."""
        body = CreateConfigProfileRequestDto(name=name, config=config)
        resp = _sdk_call(self._sdk.config_profiles.create_config_profile(body))
        return _config_profile_from_sdk(resp)

    def get_config_profile(self, uuid: str) -> ConfigProfile | None:
        """Get a config profile by UUID. Returns None only if not found."""
        try:
            resp = _sdk_call(self._sdk.config_profiles.get_config_profile_by_uuid(uuid))
            return _config_profile_from_sdk(resp)
        except RemnawaveNotFoundError:
            return None

    def list_config_profiles(self) -> list[ConfigProfile]:
        """List all config profiles."""
        resp = _sdk_call(self._sdk.config_profiles.get_config_profiles())
        items = getattr(resp, "configProfiles", []) or []
        return [_config_profile_from_sdk(profile) for profile in items]

    def find_config_profile_by_name(self, name: str) -> ConfigProfile | None:
        """Find a config profile by name. Returns None if not found."""
        for profile in self.list_config_profiles():
            if profile.name == name:
                return profile
        return None

    # --- Inbounds ---

    def list_inbounds(self) -> list[Inbound]:
        """List all inbounds across config profiles."""
        resp = _sdk_call(self._sdk.inbounds.get_all_inbounds())
        inbounds_list = getattr(resp, "inbounds", []) or []
        return [_inbound_from_sdk(ib) for ib in inbounds_list]

    # --- Internal Squads ---

    def list_internal_squads(self) -> list[dict[str, Any]]:
        """List internal squads. Returns raw dicts with uuid, name, info."""
        resp = _sdk_call(self._sdk.internal_squads.get_internal_squads())
        return [_sdk_to_dict(squad) for squad in getattr(resp, "internalSquads", []) or []]

    def get_default_squad_uuid(self) -> str:
        """Get the UUID of the Default-Squad. Returns empty string if not found."""
        squads = self.list_internal_squads()
        for s in squads:
            if isinstance(s, dict) and s.get("name") == "Default-Squad":
                return s.get("uuid", "")
        return ""

    def assign_inbounds_to_squad(self, squad_uuid: str, inbound_uuids: list[str]) -> None:
        """Assign inbounds to an internal squad (PATCH)."""
        body = UpdateInternalSquadRequestDto(uuid=UUID(squad_uuid), inbounds=[UUID(uuid) for uuid in inbound_uuids])
        _sdk_call(self._sdk.internal_squads.update_internal_squad(body))

    # --- Keygen ---

    def get_node_secret_key(self) -> str:
        """Fetch the node mTLS secret bundle from the keygen controller."""
        resp = _sdk_call(self._sdk.keygen.generate_key())
        return str(getattr(resp, "pubKey", "") or "")

    # --- Subscriptions ---

    def get_subscription_url(self, short_uuid: str) -> str:
        """Build the subscription URL for a user."""
        return f"{self._base}/api/sub/{short_uuid}"

    # --- Xray Config ---

    def get_xray_config(self) -> dict[str, Any]:
        """Get the global Xray configuration (DNS, routing, etc.)."""
        data = self._get("/api/xray/config")
        if isinstance(data, dict):
            return data
        return {}

    def update_xray_config(self, config: dict[str, Any]) -> None:
        """Update the global Xray configuration."""
        self._patch("/api/xray/config", json=config)

    # --- Auth (for initial setup — raw httpx, no SDK instance yet) ---

    @classmethod
    def login(cls, base_url: str, username: str, password: str, *, timeout: int = 30) -> str:
        """Authenticate and return an auth token."""
        import httpx

        try:
            resp = httpx.post(
                f"{base_url.rstrip('/')}/api/auth/login",
                json={"username": username, "password": password},
                timeout=timeout,
                verify=False,
            )
        except httpx.ConnectError as e:
            raise RemnawaveNetworkError(
                f"Cannot connect to panel at {base_url}",
                hint=f"Is the panel running? {e}",
                hint_type="system",
            ) from e
        except httpx.TimeoutException:
            raise RemnawaveNetworkError(
                f"Panel login timed out after {timeout}s",
                hint_type="system",
            )
        if resp.status_code != 200:
            raise RemnawaveError(
                f"Panel login failed ({resp.status_code})",
                hint="Check panel credentials",
                hint_type="user",
            )
        try:
            data = resp.json()
        except (ValueError, TypeError) as e:
            raise RemnawaveError("Panel returned invalid JSON during login", hint_type="system") from e
        if isinstance(data, dict) and "response" in data:
            data = data["response"]
        token = data.get("accessToken", "") or data.get("token", "")
        if not token:
            raise RemnawaveError("Login succeeded but no token returned", hint_type="bug")
        return token

    @classmethod
    def register_admin(cls, base_url: str, username: str, password: str, *, timeout: int = 30) -> str:
        """Register the initial admin user during setup."""
        import httpx

        try:
            resp = httpx.post(
                f"{base_url.rstrip('/')}/api/auth/register",
                json={"username": username, "password": password},
                timeout=timeout,
                verify=False,
            )
        except httpx.ConnectError as e:
            raise RemnawaveNetworkError(
                f"Cannot connect to panel at {base_url}",
                hint=f"Is the panel running? {e}",
                hint_type="system",
            ) from e
        except httpx.TimeoutException:
            raise RemnawaveNetworkError(
                f"Panel registration timed out after {timeout}s",
                hint_type="system",
            )
        if resp.status_code not in (200, 201):
            raise RemnawaveError(
                f"Admin registration failed ({resp.status_code}): {resp.text[:200]}",
                hint_type="system",
            )
        try:
            data = resp.json()
        except (ValueError, TypeError) as e:
            raise RemnawaveError("Panel returned invalid JSON during registration", hint_type="system") from e
        if isinstance(data, dict) and "response" in data:
            data = data["response"]
        token = data.get("accessToken", "") or data.get("token", "")
        if not token:
            raise RemnawaveError("Registration succeeded but no token returned", hint_type="bug")
        return token


