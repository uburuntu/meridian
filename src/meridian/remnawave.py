@dataclass
class User:
    """A Remnawave user (= Meridian client)."""

    uuid: str = ""
    short_uuid: str = ""
    username: str = ""
    vless_uuid: str = ""  # VLESS protocol UUID (used by Xray, differs from account UUID)
